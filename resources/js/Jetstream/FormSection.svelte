<script>
    import {preventDefault} from 'svelte/legacy';

    import {createEventDispatcher} from 'svelte';
    import JetSectionTitle from '@/Jetstream/SectionTitle.svelte';

    let {
        actions,
        title,
        description,
        form,
        ...rest
    } = $props();

    const dispatch = createEventDispatcher();

    let formSection = $state();

    let hasActions = $derived(actions);

    export function submit() {
        formSection.submit();
    }

    const title_render = $derived(title);
    const description_render = $derived(description);
</script>

<div class="md:grid md:grid-cols-3 md:gap-6">
    <JetSectionTitle>
        {#snippet title()}
            {@render title_render?.()}
        {/snippet}
        {#snippet description()}
            {@render description_render?.()}
        {/snippet}
    </JetSectionTitle>

    <div class="mt-5 md:mt-0 md:col-span-2">
        <form onsubmit={preventDefault(() => dispatch('submitted'))} bind:this={formSection} {...rest}>
            <div class={['px-4 py-5 bg-white sm:p-6 shadow ring-contrast dark:bg-gray-900',
                 hasActions ? 'sm:rounded-tl-md sm:rounded-tr-md' : 'sm:rounded-md'].join(' ').trim()}>
                <div class="grid grid-cols-6 gap-6">
                    {@render form?.()}
                </div>
            </div>

            {#if hasActions}
                <div
                    class="flex items-center justify-end px-4 py-3 bg-gray-100 border-t border-gray-200 text-right sm:px-6 shadow ring-contrast sm:rounded-bl-md sm:rounded-br-md dark:bg-gray-950 dark:border-gray-800">
                    {@render actions?.()}
                </div>
            {/if}
        </form>
    </div>
</div>
